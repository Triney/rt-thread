#ifdef RT_USING_I2C
 struct fm24clxx_config fm24cl64_cfg =
{
    .size  = 8 * 1024,
    .addr  = 0x50,
    .flags = 0,
};
#endif
void rt_init_thread_entry(void* parameter)
{

    rt_thread_t init_thread;
#ifdef RT_USING_COMPONENTS_INIT
    /* initialization RT-Thread Components */
    rt_components_init();
#endif

#ifdef RT_USING_SPI
    #ifdef RT_USING_W25QXX
    w25qxx_init("Flash0","spi11");
    
    #endif
#endif    
    
#ifdef RT_USING_I2C     
    fm24clxx_register("EEP","i2c1",(void *)(&fm24cl64_cfg));
#endif    
    /* Filesystem Initialization */
#if defined(RT_USING_DFS) && defined(RT_USING_DFS_ELMFAT)
    /* mount sd card fat partition 1 as root directory */
    if (dfs_mount("sd0", "/", "elm", 0, 0) == 0)
    {
        rt_kprintf("File System initialized!\n");
    }
    else
        rt_kprintf("File System initialzation failed!\n");
#endif  /* RT_USING_DFS */

#ifdef RT_USING_RTGUI
    {
        extern void rt_hw_lcd_init();
        extern void rtgui_touch_hw_init(void);

        rt_device_t lcd;

        /* init lcd */
        rt_hw_lcd_init();

        /* init touch panel */
        rtgui_touch_hw_init();

        /* find lcd device */
        lcd = rt_device_find("lcd");

        /* set lcd device as rtgui graphic driver */
        rtgui_graphic_set_device(lcd);

#ifndef RT_USING_COMPONENTS_INIT
        /* init rtgui system server */
        rtgui_system_server_init();
#endif

        calibration_set_restore(cali_setup);
        calibration_set_after(cali_store);
        calibration_init();
    }
#endif /* #ifdef RT_USING_RTGUI */
    
    #if 1
    init_thread = rt_thread_create("test_74hc595",
                                   test_74hc595_drv_thread_entry, RT_NULL,
                                   512, 31, 5);
    if (init_thread != RT_NULL)
        rt_thread_startup(init_thread);    
    #endif
    service_key_start();

    App_Rs485_CMD_Process();
    
    {
        rt_timer_t app_delay_start;

        app_delay_start = rt_timer_create("app_start", 
                        APP_LDS_Device_Init, 0, 
                        RT_TICK_PER_SECOND / 2,
                        RT_TIMER_FLAG_ONE_SHOT | RT_TIMER_FLAG_SOFT_TIMER);
        if ( RT_NULL != app_delay_start )
        {
            rt_timer_start(app_delay_start);
        }
    }
}

int rt_application_init(void)
{
    rt_thread_t init_thread;

    rt_err_t result;

    /* init led thread */
    result = rt_thread_init(&led_thread,
                            "led",
                            led_thread_entry,
                            RT_NULL,
                            (rt_uint8_t*)&led_stack[0],
                            sizeof(led_stack),
                            20,
                            5);
    if (result == RT_EOK)
    {
        rt_thread_startup(&led_thread);
    }

    

    
#if (RT_THREAD_PRIORITY_MAX == 32)
    init_thread = rt_thread_create("init",
                                   rt_init_thread_entry, RT_NULL,
                                   2048, 8, 20);
#else
    init_thread = rt_thread_create("init",
                                   rt_init_thread_entry, RT_NULL,
                                   2048, 80, 20);
#endif
    if (init_thread != RT_NULL)
        rt_thread_startup(init_thread);
    return 0;
}

